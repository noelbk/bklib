int
dump(struct netpkt *pkt) {
    int i, err=-1;
    char *p, buf[4096];
    do {
	p = buf;
	netpkt_fmt(pkt, p, sizeof(buf) - (p-buf));
	i = strlen(p);
	assertb(i>=0);
	p += i;
	*p = '\n';
	p++;
	i = strlen(memdump(p, sizeof(buf) - (p-buf), pkt->pkt_msg, pkt->pkt_len));
	assertb(i>=0);
	p += i;
	fprintf(stderr, "packet: %s\n", buf);
	err =0 ;
    } while(0);
    return err;
}

